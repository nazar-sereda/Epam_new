public class Task1 {
    public static void main(String[] args) {

        int i = 1;

        while (i<=8){
            int j = i;

            while (j>0){
                System.out.print("*");
                j--;
            }
            System.out.println(" ");
            i++;
        }

        int a = 7;

        while (a>=1){
            int b = a;

            while (b>0){
                System.out.print("*");
                b--;
            }
            System.out.println(" ");
            a--;
        }


    }
}
